<script lang="ts">
    import Header from '../../../components/Header.svelte';
    import Navigation from '../../../components/Navigation.svelte';
    import Footer from '../../../components/Footer.svelte';
    import { page } from '$app/stores';
    import type { BookResponseDto } from '$lib/book/api/book-response-dto';
    import { BookController } from '$lib/book/api/book-controller';
    import { globalServerAddress, globalJwt } from '$lib/stores';
    import { onMount } from 'svelte';
    import BookView from '$lib/book/component/BookView.svelte';

    let isbn: string;
    let serverAddress: string;
    let jwt: string;
    let bookController: BookController;
    let book: BookResponseDto;

    page.subscribe((data) => {
        isbn = data.params.isbn;
    });

    // Subscribe to global stores
    globalServerAddress.subscribe((data) => {
        serverAddress = data;
        bookController = new BookController(serverAddress, jwt);
    });
    globalJwt.subscribe((data) => {
        jwt = data;
        bookController = new BookController(serverAddress, jwt);
    });

    onMount(() => {
        bookController
            .getBook(isbn)
            .then((data) => {
                book = data;
            })
            .catch((error) => {
                console.error(error);
            });
    });
</script>

<svelte:head>
    <title>Book | bkndmvrgnzr</title>
</svelte:head>

<Header>
    <Navigation />
</Header>
<main>
    {#if book}
        <BookView {book} />
        <p>
            <a href="/book/edit/{book.isbn}">Edit book</a>
        </p>
    {:else}
        <h2>Book not found</h2>
    {/if}
</main>
<Footer />
